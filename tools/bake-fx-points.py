"""Rare-table FX bake: tiles, ember glow map, star/coin points, caustics.

Outputs:
  assets/ui/table-rare-{ember,astronomer,vault}.jpg  (768 tiles)
  assets/ui/fx-ember-glow.png     (transparent warm-vein layer, breathes via CSS)
  assets/ui/fx-caustic-{a,b}.png  (procedural seamless caustic webs)
  js/tablefx-pts.js               (window.TABLEFX_PTS: tile-relative motif points)
Rerun after swapping any rare texture.
"""
import numpy as np, json
from PIL import Image, ImageFilter

def load_tile(path, size=768):
    im = Image.open(path).convert('RGB')
    w, h = im.size; s = min(w, h)
    return im.crop(((w-s)//2, (h-s)//2, (w+s)//2, (h+s)//2)).resize((size, size), Image.LANCZOS)

def bright_points(arr, warm_only, count=36, min_dist=48):
    lum = arr.mean(2)
    warm = (arr[:,:,0] > arr[:,:,2] + 10) if warm_only else np.ones(lum.shape, bool)
    score = np.where(warm, lum, 0)
    pts = []
    s = score.copy()
    for _ in range(count):
        i = int(np.argmax(s)); y, x = divmod(i, s.shape[1])
        if s[y, x] < np.percentile(score, 92): break
        pts.append((x / s.shape[1], y / s.shape[0]))
        y0,y1 = max(0,y-min_dist), y+min_dist; x0,x1 = max(0,x-min_dist), x+min_dist
        s[y0:y1, x0:x1] = 0
    return [(round(x,4), round(y,4)) for x,y in pts]

