# ── procedural seamless caustics (wrapped-distance voronoi ridges) ──
def caustic(size, n, seed, blur=3):
    rng = np.random.RandomState(seed)
    p = rng.rand(n, 2) * size
    yy, xx = np.mgrid[0:size, 0:size].astype(np.float32)
    d = np.full((size, size), 1e9, np.float32); d2 = d.copy()
    for px, py in p:
        dx = np.abs(xx - px); dx = np.minimum(dx, size - dx)   # wrap = seamless
        dy = np.abs(yy - py); dy = np.minimum(dy, size - dy)
        dd = dx*dx + dy*dy
        closer = dd < d
        d2 = np.where(closer, d, np.minimum(d2, dd))
        d = np.where(closer, dd, d)
    ridge = 1 - np.clip((np.sqrt(d2) - np.sqrt(d)) / 18.0, 0, 1)
    ridge = (ridge ** 2.2 * 255).astype(np.uint8)
    img = Image.fromarray(ridge).filter(ImageFilter.GaussianBlur(blur))
    out = Image.merge('RGBA', (img.point(lambda v: 255), img.point(lambda v: 255), img.point(lambda v: 235), img))
    return out
caustic(640, 26, 7).save('assets/ui/fx-caustic-a.png')
caustic(640, 38, 23, blur=2).save('assets/ui/fx-caustic-b.png')

